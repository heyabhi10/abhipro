class Assignment
{
		public static void main(String args[])

		{
			int x=15;
			int y=20;

		System.out.println("x="+x);
		System.out.println("y="+y);

		//adding
		x+=y;
		System.out.println("x="+x);

		//substract
		x-=y;
                System.out.println("x="+x);
		
		//multiply
		x*=y;
                System.out.println("x="+x);

		//division
		x/=y;
                System.out.println("x="+x);

		//modulus
		x%=y;
                System.out.println("x="+x);

		//left shift
		x<<=y;
                System.out.println("x="+x);

		//right shift
		x>>=y;
                System.out.println("x="+x);

		//bitwise and
		x&=y;
                System.out.println("x="+x);

		//bitwise exclusive OR
		x^=y;
                System.out.println("x="+x);

		//bitwise inclusive OR
		x|=y;
                System.out.println("x="+x);
		}
}



